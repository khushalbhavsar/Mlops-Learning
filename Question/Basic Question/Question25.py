# 25. Find the average of three numbers.

num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
num3 = float(input("Enter the third number: "))
average = (num1 + num2 + num3) / 3 # This formula calculates the average of three numbers
print(f"The average of the three numbers is: {average}")

# Example input: 10, 20, 30
# Example output: 20.0
# Explanation:
# The average is calculated by adding the three numbers together and then dividing the sum by 3. In this case, (10 + 20 + 30) / 3 equals 60 / 3, which results in an average of 20.0.
