# 24. Find the sum of all elements in a list using `sum()`.

numbers = [5, 10, 3, 8, 2]
total_sum = sum(numbers) # This function calculates the sum of all elements in the list
print(f"The sum of all elements in the list is: {total_sum}")

# Example input: [5, 10, 3, 8, 2]
# Example output: 28
# Explanation:
# The `sum()` function takes an iterable (like a list) and returns the total sum
# of its elements. In this case, it adds up 5 + 10 + 3 + 8 + 2 to get the total of 28.

