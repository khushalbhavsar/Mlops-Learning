
# 23. Count the total number of characters in a string.

my_string = "Hello, World!"
total_characters = len(my_string) # The len() function returns the number of characters in
print(f"The total number of characters in the string is: {total_characters}")

# Example input: "Hello, World!"
# Example output: 13
# Explanation:
# The `len()` function counts all characters in the string, including letters, spaces, and punctuation. In this case, "Hello, World!" has 13 characters: 10 letters (H, e, l, l, o, W, o, r, l, d), 1 comma (,), 1 space ( ), and 1 exclamation mark (!).
